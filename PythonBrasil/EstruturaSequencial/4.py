# 4. Faça um Programa que peça as 4 notas bimestrais e mostre a média.

print("Informe 4 notas bimestrais!\n")
nota_1 = float(input("Digite a primeira nota: "))
nota_2 = float(input("\nDigite a segunda nota: "))
nota_3 = float(input("\nDigite a terceira nota: "))
nota_4 = float(input("\nDigite a quarta nota: "))

print("A média das notas eh: ", ((nota_1 + nota_2 + nota_3 + nota_4)/4))