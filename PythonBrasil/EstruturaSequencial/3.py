# 3. Faça um Programa que peça dois números e imprima a soma.

# Soma de números do tipo Inteiro
print("Informe dois números Inteiros!")
a = int(input("Digite um número: "))
b = int(input("Digite outro número: "))
print("A soma dos números inteiros é: ", (a + b))

# Soma de números do tipo Float
print("\n\nInforme dois números Float!")
a = float(input("Digite um número: "))
b = float(input("Digite outro número: "))
print("A soma dos números float é: ", (a + b))